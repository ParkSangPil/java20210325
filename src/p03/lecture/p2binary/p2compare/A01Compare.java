package p03.lecture.p2binary.p2compare;

public class A01Compare {
	public static void main(String[] args) {
		//  ==    !=     < <= > >=
		// 같다 다르다  이상이하미만초과
		// 비교 연산자는 결과가 boolean타입임
		
		int a = 3;
		int b = 8;
		
		System.out.println(a == b);
		System.out.println(a != b);
		System.out.println(a < b);
		System.out.println(a <= b);
		System.out.println(a > b);
		System.out.println(a >= b);
	}
}
